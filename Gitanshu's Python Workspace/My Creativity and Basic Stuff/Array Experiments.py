# Trying Python Arrays
# Taking Examples of Coding Languages Name.
g = ["Python","C++","Java",]

# Defining array entries to print them individually. 
g1 = "Python"
g2 = "C++"
g3 = "Java"

# len function lists the number of entries in an array.
a = len(g)
print(a)

# Whereas printing the direct array will list it's entries in the entered sequence.
print(g)
print(g1)
print(g2)
print(g3)

